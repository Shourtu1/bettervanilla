<hgroup>
	<h1>Mods Archive</h1>
	<h2>Install using an archive of mods</h2>
</hgroup>
<p>There are 4 steps:</p>
<h3>Download the modpack</h3>
<p>
	<a href="/downloads">Download</a> an archive of type <code>Mods Archive</code> that contains the correct version of the modpack.
</p>
<h3>Minecraft Instance</h3>
<p>
	Create a minecraft instance in your favourite launcher.
</p>
<h3>Quilt mod loader</h3>
<p>
	Install the correct version of the Quilt mod loader on your minecraft instance.
</p>
<h3>Install mods</h3>
<p>
	Inside of your Minecraft instance there should be a folder called <code>mods</code> create it if it does not exist and extract all .jar mods into it.
</p>